def calcular_factorial(numero):
    factorial = 1
    for n in range(1, (numero+1)):
        factorial *= n
    return factorial


def sumar(a, b):
    return a+b


def restar(a, b):
    return a-b


def multiplicar(a, b):
    return a*b


def dividir(a, b):
    return a/b